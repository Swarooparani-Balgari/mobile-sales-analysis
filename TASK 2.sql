SELECT * FROM MOBILE_SALES;

SELECT 
  SUM((QUANTITY*UNIT_PRICE)-DISCOUNT) AS TOTAL_REVENUE
FROM MOBILE_SALES;

SELECT 
  BRAND,
  SUM((QUANTITY*UNIT_PRICE)-DISCOUNT) AS TOTAL_REVENUE
FROM MOBILE_SALES
GROUP BY BRAND
ORDER BY TOTAL_REVENUE DESC;

SELECT 
  CITY,
  SUM((QUANTITY*UNIT_PRICE)-DISCOUNT) AS TOTAL_REVENUE
FROM MOBILE_SALES
GROUP BY CITY
ORDER BY TOTAL_REVENUE DESC;

SELECT
  SALES_PERSON,
  SUM((QUANTITY*UNIT_PRICE)-DISCOUNT) AS TOTAL_REVENUE
FROM MOBILE_SALES
GROUP BY SALES_PERSON
ORDER BY TOTAL_REVENUE DESC;

SELECT 
  BRAND,
  SUM(QUANTITY) AS TOTAL_UNITS_SOLD
FROM MOBILE_SALES
GROUP BY BRAND
ORDER BY TOTAL_UNITS_SOLD DESC ;
